import driver_ft260
from wlc98_register import *
import time
wlc98 = driver_ft260.ft260_dongle()
wlc98.chip_info()


print("set vout off")
wlc98.write16(I2CREG_RX_CMD,(1<<BIT_RX_VOUT_OFF))
time.sleep(1)
print("set vout on")
wlc98.write16(I2CREG_RX_CMD,(1<<BIT_RX_VOUT_ON))
time.sleep(1)
print("set vout 9100mV")
wlc98.wlc98_vout_set(9100)

