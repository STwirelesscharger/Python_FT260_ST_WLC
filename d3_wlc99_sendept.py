import driver_ft260
from wlc99_register import *

wlc99 = driver_ft260.ft260_dongle(i2c_speed = 100,dev_addr_set = 0x2C)
wlc99.chip_info()

# 0x0: EPT/nul
# 0x1: EPT/cc
# 0x2: EPT/if
# 0x3: EPT/ot
# 0x4: EPT/ov
# 0x5: EPT/oc
# 0x6: EPT/bf
# 0x8: EPT/nr
# 0xA: EPT/an
# 0xB: EPT/rst
# 0xC: EPT/rep
# 0xD: EPT/rfid
wlc99.write16(I2CREG_RX_EPT_MSG,0x0B)
wlc99.write16(I2CREG_RX_CMD,(1<<BIT_RX_SEND_EPT))#rx send ept
print("rx send ept re-ping TX will reset")
